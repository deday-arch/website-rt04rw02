// ===== RENDER TABEL IURAN =====
(function renderIuran() {
  const tbody = document.getElementById('tbodyIuran');
  if (!tbody) return;

  // Baca status dari localStorage jika ada (diset oleh admin)
  let saved = {};
  try {
    const d = JSON.parse(localStorage.getItem('rt04_iuran')) || [];
    d.forEach(x => { saved[x.noRumah] = { status: x.status || 'Belum Bayar', tglBayar: x.tglBayar || '' }; });
  } catch(e) {}

  const fmtDate = d => {
    if (!d) return '-';
    const p = d.split('-');
    const bln = ['','Jan','Feb','Mar','Apr','Mei','Jun','Jul','Agu','Sep','Okt','Nov','Des'];
    return (+p[2]) + ' ' + (bln[+p[1]] || '') + ' ' + p[0];
  };

  let jmlLunas = 0, totalTerkumpul = 0;

  tbody.innerHTML = ALL_WARGA.map((w, i) => {
    const info    = saved[w.no] || { status: 'Belum Bayar', tglBayar: '' };
    const isLunas = info.status === 'Lunas';
    if (isLunas) { jmlLunas++; totalTerkumpul += 50000; }
    const badge = isLunas
      ? '<span class="status-lunas">Lunas</span>'
      : '<span class="status-belum">Belum Bayar</span>';
    return `<tr>
      <td>${i + 1}</td>
      <td style="font-size:.82rem">${w.no}</td>
      <td>${w.nama}</td>
      <td style="font-size:.82rem;color:#666">${w.hp}</td>
      <td>Rp 30.000</td>
      <td>Rp 20.000</td>
      <td><strong>Rp 50.000</strong></td>
      <td>${badge}</td>
      <td>${fmtDate(info.tglBayar)}</td>
    </tr>`;
  }).join('');

  // Update stat cards
  const jmlBelum = ALL_WARGA.length - jmlLunas;
  const fmtRp = n => 'Rp ' + n.toString().replace(/\B(?=(\d{3})+(?!\d))/g, '.');
  const elL = document.getElementById('statLunas');
  const elB = document.getElementById('statBelum');
  const elT = document.getElementById('statTotal');
  if (elL) elL.textContent = jmlLunas;
  if (elB) elB.textContent = jmlBelum;
  if (elT) elT.textContent = fmtRp(totalTerkumpul);
})();

// ===== FILTER TABEL =====
function filterTable() {
  const cari   = (document.getElementById('searchWarga')?.value || '').toLowerCase();
  const status = (document.getElementById('filterStatus')?.value || '');
  document.querySelectorAll('#iuranTable tbody tr').forEach(row => {
    const no    = row.cells[1]?.textContent.toLowerCase() || '';
    const nama  = row.cells[2]?.textContent.toLowerCase() || '';
    const st    = row.cells[7]?.textContent.trim() || '';
    const okS   = !cari   || no.includes(cari) || nama.includes(cari);
    const okSt  = !status || st === status;
    row.style.display = okS && okSt ? '' : 'none';
  });
}
